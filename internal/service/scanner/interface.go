package scanner

import "context"

// Scanner — контракт сервиса сканера (оркестратор).
// Не содержит доменной логики; управляет жизненным циклом и связывает юзкейсы/адаптеры.
// Сервис не владеет внешними каналами (за закрытие отвечает вызывающая сторона).
//
// Реализации должны корректно завершаться по отмене контекста.
type Scanner interface {
	Start(ctx context.Context) error
}
